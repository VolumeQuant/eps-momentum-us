# -*- coding: utf-8 -*-
"""fwd_PER 절대값 + gap 배수 스위트스팟 EDA 결과 → 개인봇. 중학생 눈높이 + 수치/예시."""
import os, sys, urllib.request, urllib.parse
sys.stdout.reconfigure(encoding='utf-8')

msg = """📊 <b>[fwd_PER·gap 스위트스팟 연구 — 쉽게+예시]</b>
2026-06-30 · 미국 시스템 / 우리가 커버하는 종목 98일 데이터

<b>형 질문</b>: "fwd_PER(주가÷내년예상이익)이 47~69로 높네? 한국은 20 넘으면 별로던데 미국은?"
🔸 예: 루멘텀 47 · 타워세미 57 · 코닝 69 · 노키아 29.

<b>━━ 답: 미국은 정반대였어요 ━━</b>
한국 직감("20 넘으면 나쁨")이 <b>미국 우리 종목엔 안 통해요.</b> 오히려 비싼 게 더 올랐어요. 단 진짜 나쁜 구간이 따로 있었어요.

<b>━━ ① fwd_PER이 높으면 나빠질까? (40일 뒤 수익) ━━</b>
거래 후보 종목들을 fwd_PER 구간으로 나눠봤어요:
• 0~15(아주쌈): <b>+43%</b> 👍 (싼 대박주 — 옛날 SNDK·MU 같은 거)
• <b>15~20: −1%, 승률 32%</b> ❌ ← 유일하게 손해! "죽음의 구간"
• 20~30: +18~27%
• <b>30~55: +33%, 승률 92%</b> 👍 ← 제일 셈
• 55~80: +19% (조금 식음)

➡️ 나쁜 건 "20 위"가 아니라 <b>"15~20 어정쩡 구간"</b>. 안 싸지도 안 성장하지도 않는 애매한 애들이 제일 나빴어요. <b>30~55는 오히려 최강.</b>

<b>━━ ② 그럼 "비싸지면 판다(이탈선 30)"는 맞나? ━━</b>
순위에서 밀려난 종목만 따로 봤어요(=이탈 규칙이 작동하는 애들):
• 25~30: +26% / <b>30~40: +35%(승률 93%)</b> / 40~55: +24%
😮 순위 밀려도 <b>비싼(30~55) 애들이 제일 잘 올랐어요.</b> 지금 규칙은 이걸 30에서 파는데, <b>이 강세장만 보면 손해</b>였던 셈.
<b>근데 안 바꿔요.</b> 왜냐면 이 데이터는 <b>5개월 전부 오른 장(강세장)</b>이라, "비싼 거 오래 들고 있으면 이득"은 당연한 결과예요. 30은 <b>나중에 시장이 꺾일 때 거품을 먼저 던지는 보험선</b>이고, 그 약세장이 이 데이터엔 아예 없어서 보험값을 못 따져요. 🔸 형이 늘 말하는 "한두 종목·짧은 강세장 보고 규칙 바꾸지 마라"가 정확히 이 상황.

<b>━━ ③ gap(=PER÷fwdPER=기대성장 배수)이 클수록? ━━</b>
gap은 "내년 이익이 올해보다 몇 배 뛰냐"예요. 클수록 이익 폭증.
• 1~1.5배: +17% / 1.5~2: +25% / 2~2.5: <b>+43%</b> / 3~4: +39% / 6배+: <b>+58%(승률 100%)</b>
➡️ <b>대체로 gap 클수록 좋음</b>(이익 폭증주가 강함). 완벽한 직선은 아니고 군데군데 출렁임.

<b>━━ ④ 둘을 합친 스위트스팟 (2D 격자) ★ ━━</b>
가장 센 조합 2개:
🥇 <b>fwd_PER 0~15 + gap 3.5배+ = +87%(승률 100%)</b> ← 싼데 이익 폭증(딥밸류 턴어라운드)
🥈 <b>fwd_PER 30~55 = 어느 gap이든 +31~55%</b> ← 비싼 성장주(승률 90~100%)
🚫 피할 곳: <b>fwd_PER 15~20 = gap 뭐든 −5~+3%</b> (죽음의 구간, 2D로도 확인)

<b>━━ ⑤ 형이 물은 광통신주 대입 ━━</b>
이들 fwd_PER이 <b>47~69 → 대부분 30~55 강세 구간</b>에 들어가요(역사적 +27~33%, 승률 90%+).
🔸 루멘텀 47·타워세미 57 = 좋은 자리 / 코닝 69 = 60 넘어 "조금 식는" 경계 / 노키아 29·패브리넷 31 = 밸류는 제일 안전.
➡️ <b>이 장에선 광통신주의 높은 fwd_PER은 빨간불이 아니에요.</b>

<b>━━ 솔직한 한계 (제일 중요) ━━</b>
• 데이터가 <b>5개월·강세장 하나뿐</b>. 승률 90~100%는 다 오른 장이라 그래요(구간끼리 순서가 신호지 절대 승률 아님).
• 한국의 "20 넘으면 나쁨"은 <b>7.4년(약세장 포함)</b> 결과 — 기간이 달라 직접 비교는 사과 vs 오렌지.
• 그래서 <b>시스템은 안 바꿔요(관찰만).</b> 약세장 오면 미국도 비싼 게 먼저 깨질 수 있는데 이 데이터론 검증 불가.

<b>━━ 한 줄 ━━</b>
<b>미국 우리 종목은 "fwd_PER 15~20만 피하고, 싼데 이익폭증(gap 큼) 또는 fwd_PER 30~55"가 스위트스팟.</b> 광통신주는 그 좋은 자리에 있어요. 단 강세장 한정 관찰이라 규칙은 그대로 둡니다."""

tok = os.environ.get('TELEGRAM_BOT_TOKEN'); pid = os.environ.get('TELEGRAM_PRIVATE_ID')
if not tok or not pid:
    print('시크릿 없음 — 미리보기:\n'); print(msg); sys.exit(0)
parts, buf = [], ''
for line in msg.split('\n'):
    if len(buf) + len(line) + 1 > 3900:
        parts.append(buf); buf = ''
    buf += line + '\n'
if buf:
    parts.append(buf)
for i, p in enumerate(parts):
    data = urllib.parse.urlencode({'chat_id': pid, 'text': p, 'parse_mode': 'HTML'}).encode()
    r = urllib.request.urlopen(urllib.request.Request(f'https://api.telegram.org/bot{tok}/sendMessage', data=data))
    print(f'발송 {i+1}/{len(parts)}:', r.status)
