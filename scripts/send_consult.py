def send(token, chat_id, text):
    chunks, rem = [], text.strip()
    while rem:
        if len(rem) <= 3800:
            chunks.append(rem); break
        sp = rem[:3800].rfind('\n')
        if sp <= 0:
            sp = 3800
        chunks.append(rem[:sp]); rem = rem[sp:].strip()
    for i, c in enumerate(chunks):
        data = urllib.parse.urlencode({
            'chat_id': chat_id, 'text': c,
            'parse_mode': 'HTML', 'disable_web_page_preview': 'true',
        }).encode()
        url = f'https://api.telegram.org/bot{token}/sendMessage'
        with urllib.request.urlopen(urllib.request.Request(url, data=data), timeout=30) as r:
            res = json.loads(r.read().decode())
            print(f'chunk {i+1}/{len(chunks)} ok={res.get("ok")}')


if __name__ == '__main__':
    token = os.environ['TELEGRAM_BOT_TOKEN']
    chat_id = os.environ['TELEGRAM_PRIVATE_ID']
    send(token, chat_id, MSG)
    print('done')
